    private void loadComboBox(){
        try{
            List<Consumer> lista = cr.findAll();
            ccbm.addList(lista);
            CMBConsumer.setModel(ccbm);
            CMBConsumer.repaint();
        }catch(CrudFormException ex){
            JOptionPane.showMessageDialog(this,ex.getMessage());
        }
    }
    
    private boolean check(){
        if(firstNameField.getText() == null || firstNameField.getText().trim().isEmpty()){
            return false;
        }
        if(lastNameField.getText() == null || lastNameField.getText().trim().isEmpty()){
            return false;
        }
        if(cityField.getText() == null || cityField.getText().trim().isEmpty() ){
            return false;
        }
        if(emailField.getText() == null || emailField.getText().trim().isEmpty()){
            return false;
        }
        if (adressField.getText() == null || adressField.getText().trim().isEmpty()) {
            return false;
        }
        if (codeField.getText() == null || codeField.getText().isEmpty()) {
            return false;
        }
        if(numberField.getText() == null || numberField.getText().isEmpty()){
            return false;
        }
        
        return true;
    }
    
