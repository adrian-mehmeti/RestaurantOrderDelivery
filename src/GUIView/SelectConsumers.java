    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        // TODO add your handling code here:
        
        c = (Consumer)(ccbm.getSelectedItem());
        if(c != null){
            CreatesDeliveryUser cdu = new CreatesDeliveryUser();
            mainDesktopPane.add(cdu);
            cdu.show();
        }else{
            if(check()){
                try{
                c = new Consumer();
                c.setName(firstNameField.getText());
                c.setSurname(lastNameField.getText());
                c.setCity(cityField.getText());
                c.setEmail(emailField.getText());
                c.setAdress(adressField.getText());
                c.setPostCode(Integer.parseInt(codeField.getText()));
                c.setTelephone(Integer.parseInt(numberField.getText()));
                cr.create(c);
                CreatesDeliveryUser cdu = new CreatesDeliveryUser();
                mainDesktopPane.add(cdu);
                cdu.show();
                }catch(Exception ex){
                    JOptionPane.showMessageDialog(this,ex.getMessage());
                }
             }else{
            JOptionPane.showMessageDialog(this,"Ju lutem selektoni ose plotesoni te gjitha fushat per konsumatoret");
            }
        }
       
    }//GEN-LAST:event_submitButtonActionPerformed

